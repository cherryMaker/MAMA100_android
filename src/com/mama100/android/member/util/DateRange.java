package com.mama100.android.member.util;

import java.util.Date;

/**
 * 时间段对象
 * @author jimmy
 */
public class DateRange {
	
	private Date from;

	private Date to;

	public Date getFrom() {
		return from;
	}

	public void setFrom(Date from) {
		this.from = from;
	}

	public Date getTo() {
		return to;
	}

	public void setTo(Date to) {
		this.to = to;
	}
	
}
